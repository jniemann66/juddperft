inline Bitboard fillKingAttacksOccluded(Bitboard g, Bitboard p)
{
	Bitboard a, b;
	Bitboard t, u;
	a = g; t = g; t <<= 1; t &= LEFTMASK; a |= t;
	b = a; b <<= 8; a |= b; u = a; u >>= 1; u &= RIGHTMASK; a |= u;
	b = a; b >>= 8; a |= b; a &= ~g; a &= p;
	return a;
}

